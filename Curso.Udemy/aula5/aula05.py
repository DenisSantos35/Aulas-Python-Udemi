"""
operadores aritimeticos
+ - * /
// ** %
()
"""
print(10*10) #multiplicaçao
print('10' * 10) #repeticao inteiro * string
print(10+10) #adicao
print('5' + '5') #concatenaçao string + string  
print(10 - 5) #subtracao
print(10 / 2) #divisao
print(10//7) #divisao inteira
print(10%6) #mod resto da divisao
print(10**2) #exponenciaçao
print(5+2*10)#precedencia
print((5+2)*10)#alterada precendencia com parenteses()
