'''
Tipos int float
int ~> Numero inteiro
O tipo int representa qualquer numero
positivo e ou negativo . int sem sina -e considerado 
positivo.
'''
print(11, type(11)) #int
print(-11, type(-11)) #int
print(0, type(0)) #int

'''
Float ~> numero com ponto flutuante
O tipo float representa qualquer numero
positivo ou negativo com ponto flutuante.
float sem sinal e considerado positivo.
'''
print(11.1, type(11.1))
print(-11.1, type(-11.41))
'''
A função type mostra o valor que opython inferiu o valor.
'''

print(type(-11.41), type(11))


